#include "3-calc.h"

/**
 * op_add - function which computes the sum of two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that computes the difference of two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that computes the product of two integers
 * @a: integer1
 * @b: integer2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that computes the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that computes the remainder of the division of two integers
 * @a: integer1
 * @b: integer2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
